	private void register(int id, Class<?> messageClass, Class<?> handlerClass){
		messages.put(id, messageClass);
		if(handlerClass!=null) handlers.put(id, handlerClass);
	}
	
	/**
	 * 获取消息体
	 * @param id
	 * @return
	 * @throws InstantiationException
	 * @throws IllegalAccessException
	 */
	public Message getMessage(int id) throws InstantiationException, IllegalAccessException{
		if(!messages.containsKey(id)){
			return null;
		}else{
			return (Message)messages.get(id).newInstance();
		}
	}
	
	/**
	 * 获取处理函数
	 * @param id
	 * @return
	 * @throws InstantiationException
	 * @throws IllegalAccessException
	 */
	public Handler getHandler(int id) throws InstantiationException, IllegalAccessException{
		if(!handlers.containsKey(id)){
			return null;
		}else{
			return (Handler)handlers.get(id).newInstance();
		}
	}

	public static void main(String[] s) {
		String src = "D:\\java\\work\\tag\\v1.0\\Polar\\PolarServer\\src\\com\\game\\message\\pool\\MessagePool.java";
		BufferedReader reader = null;
		try {
			reader = new BufferedReader(new FileReader(new File(src)));
		} catch (FileNotFoundException e1) {
			e1.printStackTrace();
		}
		String line = null;
		List<MessageModel> list = new ArrayList<MessageModel>();
		try {
			while ((line = reader.readLine()) != null) {
				if (line.trim().startsWith("register")) {
					String[] split = line.split(",");
					String tipName = split[1].substring(1, split[1].indexOf("Message"));
					String number = split[0].trim().substring(9);
					list.add(new MessageModel(line.trim(), number, tipName));
				}
			}
			java.util.Collections.sort(list, new Comparator<MessageModel>() {

				@Override
				public int compare(MessageModel arg0, MessageModel arg1) {
					if (Integer.parseInt(arg0.number) > Integer.parseInt(arg1.number)) {
						return 1;
					}
					return -1;
				}
				
			});
			System.out.println("**********MessagePool*******************************************************************************************");
			for (MessageModel model : list) {
				System.out.println(model.line.replace(model.number, "ServerProtocol." + model.tipName));
			}
			System.out.println("\r\n**********ServerProtocol******************************************************************************");
			for (MessageModel model : list) {
				System.out.println("public static final int " + model.tipName + " = " + model.number + ";");
			}
		} catch (IOException e) {
			e.printStackTrace();
		}
	}

